package entity;

public class CompletionAnswer {
	private int caAnswerId;
	private int questionId;
	private int paperId;
	private int answerId;
	private int caAnswer;

	public int getCaAnswerId() {
		return caAnswerId;
	}

	public void setCaAnswerId(int caAnswerId) {
		this.caAnswerId = caAnswerId;
	}

	public int getQuestionId() {
		return questionId;
	}

	public void setQuestionId(int questionId) {
		this.questionId = questionId;
	}

	public int getPaperId() {
		return paperId;
	}

	public void setPaperId(int paperId) {
		this.paperId = paperId;
	}

	public int getAnswerId() {
		return answerId;
	}

	public void setAnswerId(int answerId) {
		this.answerId = answerId;
	}

	public int getCaAnswer() {
		return caAnswer;
	}

	public void setCaAnswer(int caAnswer) {
		this.caAnswer = caAnswer;
	}
}
